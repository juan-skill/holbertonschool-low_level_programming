#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: number one
 * @b: number two
 * Return: sum of the two
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 * @a: number one
 * @b: number two
 * Return: rest of the two
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two numbers
 * @a: number one
 * @b: number two
 * Return: multiply of the two
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: number one
 * @b: number two
 * Return: divide of the two
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - module two numbers
 * @a: number one
 * @b: number two
 * Return: modules of the two
 */
int op_mod(int a, int b)
{
	return (a % b);
}
